/* Задание (занятие 6): 
1. Пусть задан некоторый пользователь (допустим пользователь с id=3). 
Из всех друзей этого пользователя найдите человека, который больше всех общался с нашим пользователем (id=1).
 */

select 
	from_user_id,
	count(*)
from messages
where to_user_id = 1 and ((from_user_id in ((SELECT initiator_user_id FROM friend_requests 
       WHERE ((target_user_id = 3) and status = 'approved')))) or (from_user_id in ((SELECT target_user_id FROM friend_requests 
       WHERE ((initiator_user_id = 3) and status = 'approved')))))
group by from_user_id
order by count(*) desc
limit 1;


